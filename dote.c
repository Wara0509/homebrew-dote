int color(int x){
    if (x == 0) return 0;      // 黒（背景）
    else if (x == 10) return 10;
    else if (x == 90) return 90;
    else if (x == 118) return 118;
    else if (x == 223) return 223;
    else if (x == 52) return 52;
    else if (x == 1) return 1;
    else if (x == 11) return 11;
    else return 24;            // それ以外は24にフォールバック
}

void dote(int k){
    printf("\e[H\e[J\e[?7l");
    if (k%2==0)
    {
        printf("\n \n \n \n \n");
    }
    for (int i = 0; i < 16; i++)
    {
        printf("%*s",k, "");
        for (int j = 0; j < 16; j++)
        {
            int x=a [i][j];
            if (k%2==1)
            {
                x=b[i][j];
            }
            printf("\e[48;5;%dm",color(x));
            printf("  ");
            printf("\e[0m");
        }
        printf("\n");
    }
    usleep(125000);
}
